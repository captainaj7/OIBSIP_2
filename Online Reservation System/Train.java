    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        
        String tno = Tno.getText();
        String tname = Tname.getText();
        String splace = Tstart.getText();
        String dest= Tdest.getText();
        String price = Tprice.getText();
        
        try {
            pst = con.prepareStatement("Insert into traindetails(Tno,Tname,Tstart,Tdest,Tprice)values(?,?,?,?,?)");
            pst.setString(1,tno);
            pst.setString(2,tname);
            pst.setString(3,splace);
            pst.setString(4,dest);
            pst.setString(5,price);
            int k =pst.executeUpdate();
            
            if(k==1)
            {
                JOptionPane.showMessageDialog(this, "RECORD ADDED");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "RECORD FAILED");
            }
            
            
            Tno.setText("");
            Tname.setText("");
            Tstart.setText("");
            Tdest.setText("");
            Tprice.setText("");
            
            Tno.requestFocus();
            
            jButton1.setEnabled(false);
            
        } catch (SQLException ex) {
            Logger.getLogger(Train.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }                                        

